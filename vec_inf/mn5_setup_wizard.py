def parse_existing_launch_env(path: Path) -> dict[str, str]:
    """Load simple KEY=VALUE pairs from an existing .launch.env file."""
    if not path.exists():
        return {}

    values: dict[str, str] = {}
    for raw_line in path.read_text(encoding="utf-8").splitlines():
        line = raw_line.strip()
        if not line or line.startswith("#") or "=" not in line:
            continue
        key, raw_value = line.split("=", 1)
        key = key.strip()
        raw_value = raw_value.strip()
        if raw_value == "":
            values[key] = ""
            continue
        try:
            tokens = shlex.split(raw_value, posix=True)
        except ValueError:
            values[key] = raw_value.strip("\"'")
            continue
        values[key] = tokens[0] if len(tokens) == 1 else " ".join(tokens)
    return values


